print("Hello! I'm here to help you calculate the average of two numbers.")

# Get the first number from the user
number1 = float(input("Enter the first number: "))

# Get the second number from the user
number2 = float(input("Enter the second number: "))

# Calculate the average using the formula
average = (number1 + number2) / 2

# Display the average with two decimal places
print("The average of the two numbers is:", format(average, '.2f'))

