# Welcome the user to the program
print("Welcome to the Triangle Area Calculator!")

# Get the base and height measurements from the user
base = float(input("Enter the base of the triangle: "))
height = float(input("Enter the height of the triangle: "))

# Calculate the area using the formula
area = (base * height) / 2

# Display the calculated area using a clear message
print("The area of the triangle is:", area)

# Add a closing message for a friendly touch
print("Thank you for using the Triangle Area Calculator!")
